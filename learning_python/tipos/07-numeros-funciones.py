import math  # modulo importado (math.py). codigo escrito en python

# redondea el numero al que esta mas cercano
print(round(1.3))
print(round(1.7))
print(round(1.5))

# devuelve el valor absoluto
print(abs(-77))
print(abs(55))

print(math.ceil(1.1))
print(math.floor(1.9999999))
print(math.isnan(23))
# print(math.isnan("23"))
print(math.pow(10, 3))  # eleva el numero
print(math.sqrt(9))  # saca la raiz cuadrada
